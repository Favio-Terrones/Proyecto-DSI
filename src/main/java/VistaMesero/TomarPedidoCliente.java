    private void CargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CargarActionPerformed
        DefaultTableModel modelo = new DefaultTableModel(); //agregar las filas a mi tabla
        tbInformacionPlatillos.setModel(modelo);
        String campo = (String) CbDia.getSelectedItem();
        String where = "";
        if (!"".equals(campo)) { //Si el campo no est√° vacio
            where = "where diaPlatillo='" + campo + "'";
        }
        PreparedStatement ps = null; //hacer consulta a la bd
        ResultSet rs = null; //Obtener resultados de la consulta en la bd
        try {
            Conexion con = new Conexion();
            Connection conexion = con.getConnection();
            ps = conexion.prepareStatement("select idPlatillo, codigoPlatillo,diaPlatillo,nombrePlatillo,precioPlatillo,disponibilidadPlatillo from platillo " + where);
            rs = ps.executeQuery();
            modelo.addColumn("id");
            modelo.addColumn("Codigo");
            modelo.addColumn("Dia");
            modelo.addColumn("Nombre");
            modelo.addColumn("Precio");
            modelo.addColumn("Disponibilidad");
            ResultSetMetaData rsMD = rs.getMetaData();
            int cantidadColumnas = rsMD.getColumnCount(); 
            //ANCHO DE LAS COLUMNAS
            int anchos[] = {20, 50, 80, 100, 50, 100};
            for (int i = 0; i < cantidadColumnas; i++) {
                tbInformacionPlatillos.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
            }
            while (rs.next()) {
                Object fila[] = new Object[cantidadColumnas]; 
                for (int i = 0; i < cantidadColumnas; i++) {
                    fila[i] = rs.getObject(i + 1);
                }
                modelo.addRow(fila);
            }
        } catch (SQLException ex) {
            System.err.println("Error, " + ex);
        }


    }//GEN-LAST:event_CargarActionPerformed

    private void tbInformacionPlatillosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbInformacionPlatillosMouseClicked
        PreparedStatement ps = null; 
        ResultSet rs = null; 
        try {
            Conexion con = new Conexion();
            Connection conexion = con.getConnection();
            int fila = tbInformacionPlatillos.getSelectedRow();
            String id = tbInformacionPlatillos.getValueAt(fila, 0).toString();
            ps = conexion.prepareStatement("select idPlatillo, diaPlatillo, nombrePlatillo, precioPlatillo from platillo where idPlatillo=?");
            ps.setString(1, id);
            rs = ps.executeQuery();
            while (rs.next()) {
                txtNamePlatillo.setText(rs.getString("nombrePlatillo"));
                txtDia.setText(rs.getString("diaPlatillo"));
                IdPlatillo.setText(rs.getString("idPlatillo"));
                Dia.setText(rs.getString("diaPlatillo"));
                Nombre.setText(rs.getString("nombrePlatillo"));
                Precio.setText(String.valueOf(rs.getString("precioPlatillo")));
            }
        } catch (SQLException ex) {
            System.err.println("Error, " + ex);
        }
    }//GEN-LAST:event_tbInformacionPlatillosMouseClicked

