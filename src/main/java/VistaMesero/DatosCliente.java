    BoletaPresencial B = BoletaPresencial.getInstancia();
    public void Totalizar() {
        double t = 0;
        double p = 0;
        if (B.TablaBoleta.getRowCount() > 0) {
            for (int i = 0; i < B.TablaBoleta.getRowCount(); i++) {
                p = Double.parseDouble(B.TablaBoleta.getValueAt(i, 4).toString());
                t += p;
            }
            B.Total.setText(String.valueOf(t));
        }
    }
    private void PAGARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PAGARActionPerformed
        Totalizar();
        B.Mesa.setText(Num.getText());
        B.DNI.setText(DNI.getText());
        B.Cliente.setText(Nombre.getText());
        this.dispose();
    }//GEN-LAST:event_PAGARActionPerformed

