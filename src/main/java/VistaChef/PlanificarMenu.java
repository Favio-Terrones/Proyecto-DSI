    private void TablaPlatilloMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaPlatilloMouseClicked
        PreparedStatement ps = null; //hacer consulta a la bd
        ResultSet rs = null; //Obtener resultados de la consulta en la bd
        
        try {
            Conexion con = new Conexion();
            Connection conexion = con.getConnection();
            
            int fila = TablaPlatillo.getSelectedRow();
            String codigo = TablaPlatillo.getValueAt(fila, 0).toString();
            
            ps = conexion.prepareStatement("select  codigoPlatillo,diaPlatillo, nombrePlatillo, precioPlatillo, observacionesPlatillo, disponibilidadPlatillo from platillo where codigoPlatillo=?");
            ps.setString(1, codigo);
            rs = ps.executeQuery();
            
            while(rs.next()) {
                Codigo.setText(rs.getString("codigoPlatillo"));
                Nombre.setText(rs.getString("nombrePlatillo"));
                Precio.setText(String.valueOf(rs.getString("precioPlatillo"))); 
                Disponibilidad.setText(rs.getString("disponibilidadPlatillo"));
                Observaciones.setText(rs.getString("observacionesPlatillo"));
                Dia.setText(rs.getString("diaPlatillo"));
            }
            
        } catch (SQLException ex) {
            System.err.println("Error, "+ex);
        }
     
    }//GEN-LAST:event_TablaPlatilloMouseClicked

    private void CargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CargarActionPerformed
        DefaultTableModel modelo = new DefaultTableModel(); //agregar las filas a mi tabla
        TablaPlatillo.setModel(modelo);
        
        String campo = CodigoB.getText();
        String where = "";
        
        if(!"".equals(campo)) { //Si el campo no estÃ¡ vacio
            where = "where codigoPlatillo='"+campo+"'";
        } 
        
        PreparedStatement ps = null; //hacer consulta a la bd
        ResultSet rs = null; //Obtener resultados de la consulta en la bd
        try {
            Conexion con = new Conexion();
            Connection conexion = con.getConnection();

            ps = conexion.prepareStatement("select codigoPlatillo,diaPlatillo,nombrePlatillo,precioPlatillo,observacionesPlatillo,disponibilidadPlatillo from platillo "+where);
            rs = ps.executeQuery();
            
            modelo.addColumn("Codigo");
            modelo.addColumn("Dia");
            modelo.addColumn("Nombre");
            modelo.addColumn("Precio");
            modelo.addColumn("Observaciones");
            modelo.addColumn("Disponibilidad");
            
            
            //Le asignamos todo lo que hemos obtenido en nuestra BD
            ResultSetMetaData rsMD = rs.getMetaData();
            int cantidadColumnas = rsMD.getColumnCount(); //Conteo exacto de mis columnas en mi tabla
            
            
            //ANCHO DE LAS COLUMNAS
            int anchos[] = {70,60,100,50,100,100};
            for (int i = 0; i < cantidadColumnas; i++) {
                //Traemos a nuestra tabla, vamos a obtener el modelo de las columnas
                //Vamos obtner cada una de las columnas y vamos a establacer el ancho predefinido
                TablaPlatillo.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
            }           
            while(rs.next()) { //mientras aÃºn haya registros en mi bd llenarÃ¡ la tabla
                Object fila[] = new Object[cantidadColumnas]; //creamos arreglo para cada una de las filas que vamos obteniendo
                for (int i = 0; i < cantidadColumnas; i++) {
                    fila[i]=rs.getObject(i+1); 
                }
                modelo.addRow(fila);
            } 
            CodigoB.setText(null);
            
        } catch(SQLException ex) {
            System.err.println("Error, "+ex);
        }
    }//GEN-LAST:event_CargarActionPerformed

